#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
WORKSPACE_DIR="${OPENCLAW_WORKSPACE:-$ROOT_DIR}"
DB_PATH="${WORKCTL_DB_PATH:-$ROOT_DIR/data/workos.db}"
PROJECTS_ROOT="${WORKCTL_PROJECTS_ROOT:-$WORKSPACE_DIR/work/projects}"
CUSTOMERS_ROOT="${WORKCTL_CUSTOMERS_ROOT:-$WORKSPACE_DIR/work/customers}"

rm -f "$DB_PATH"
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" \
WORKCTL_DB_PATH="$DB_PATH" \
WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" \
WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" \
"$ROOT_DIR/bin/workctl" init >/dev/null
sqlite3 "$DB_PATH" < "$ROOT_DIR/examples/demo-seed.sql"

OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" customer list >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" contact list --customer "Northwind Systems" >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" project list >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" offer list --customer "Northwind Systems" >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" offer item list --offer QDEMO001 >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" activity list --project PR0002 >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" task create --project PR0002 --title "Smoke task" >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" task list --project PR0002 >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" task comment add --task 1 --body "smoke" >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" task status --task 1 --status in_progress >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" task show --task 1 >/dev/null
OPENCLAW_WORKSPACE="$WORKSPACE_DIR" WORKCTL_DB_PATH="$DB_PATH" WORKCTL_PROJECTS_ROOT="$PROJECTS_ROOT" WORKCTL_CUSTOMERS_ROOT="$CUSTOMERS_ROOT" "$ROOT_DIR/bin/workctl" search --keyword northwind >/dev/null

echo "Smoke test passed"
